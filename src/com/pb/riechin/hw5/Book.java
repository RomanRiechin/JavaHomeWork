package com.pb.riechin.hw5;

    public class Book {

        private String title;
        private String author;
        private int dateWriter;

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }

        public String getAuthor() {
            return author;
        }

        public void setAuthor(String author) {
            this.author = author;
        }

        public int getDateWriter() {
            return dateWriter;
        }

        public void setDateWriter(int dateWriter) {
            this.dateWriter = dateWriter;
        }

    }